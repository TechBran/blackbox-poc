def get_disk_usage() -> Dict[str, Any]:
    """Get disk usage statistics for monitoring."""
    import shutil

    usage = {}

    # Overall disk usage
    stat = shutil.disk_usage("/")
    usage["disk"] = {
        "total_gb": stat.total / (1024 ** 3),
        "used_gb": stat.used / (1024 ** 3),
        "free_gb": stat.free / (1024 ** 3),
        "percent": (stat.used / stat.total) * 100
    }

    # Archive directory size
    archive_size = 0
    archive_count = 0
    if ARC_DIR.exists():
        for f in ARC_DIR.glob("*.txt"):
            archive_size += f.stat().st_size
            archive_count += 1

    usage["archives"] = {
        "count": archive_count,
        "size_mb": archive_size / (1024 ** 2)
    }

    # Uploads directory size
    uploads_size = 0
    uploads_count = 0
    if UPLOADS_DIR.exists():
        for f in UPLOADS_DIR.rglob("*"):
            if f.is_file():
                uploads_size += f.stat().st_size
                uploads_count += 1

    usage["uploads"] = {
        "count": uploads_count,
        "size_mb": uploads_size / (1024 ** 2)
    }

    # Current volume size
    if VOL_PATH.exists():
        usage["volume_mb"] = VOL_PATH.stat().st_size / (1024 ** 2)
    else:
        usage["volume_mb"] = 0

    # Database size
    db_path = Path("Portal/tasks.db")
    if db_path.exists():
        usage["database_mb"] = db_path.stat().st_size / (1024 ** 2)
    else:
        usage["database_mb"] = 0

    # Add warnings
    usage["warnings"] = []
    if usage["disk"]["percent"] > 90:
        usage["warnings"].append("CRITICAL: Disk usage above 90%")
    elif usage["disk"]["percent"] > 80:
        usage["warnings"].append("WARNING: Disk usage above 80%")

    if usage["disk"]["free_gb"] < 5:
        usage["warnings"].append("WARNING: Less than 5GB free space")

    if usage["archives"]["count"] > 300:
        usage["warnings"].append("INFO: Many archive files, cleanup recommended")

    return usage

def cleanup_stuck_tasks(max_hours: int = 24) -> Dict[str, Any]:
    """Clean up tasks that have been stuck in processing/pending for too long."""
    from datetime import datetime, timezone, timedelta

    cleanup_stats = {
        "checked": 0,
        "marked_failed": 0,
        "failed_tasks": []
    }

    try:
        import sqlite3
        db_path = Path("Portal/tasks.db")
        if not db_path.exists():
            return cleanup_stats

        conn = sqlite3.connect(str(db_path))
        cursor = conn.cursor()

        # Find tasks stuck in processing or pending
        cursor.execute("""
            SELECT task_id, task_type, status, created_at, updated_at, prompt
            FROM tasks
            WHERE status IN ('processing', 'pending')
        """)

        stuck_tasks = []
        now = datetime.now(timezone.utc)
        cutoff = now - timedelta(hours=max_hours)

        for row in cursor.fetchall():
            task_id, task_type, status, created_at, updated_at, prompt = row
            cleanup_stats["checked"] += 1

            # Parse the updated_at timestamp
            try:
                updated_time = datetime.fromisoformat(updated_at.replace('Z', '+00:00'))

                # If task hasn't been updated in max_hours, mark as stuck
                if updated_time < cutoff:
                    stuck_tasks.append({
                        "task_id": task_id,
                        "task_type": task_type,
                        "status": status,
                        "hours_stuck": (now - updated_time).total_seconds() / 3600
                    })
            except Exception as e:
                # If we can't parse the time, assume it's old and mark it
                stuck_tasks.append({
                    "task_id": task_id,
                    "task_type": task_type,
                    "status": status,
                    "hours_stuck": "unknown"
                })

        # Mark stuck tasks as failed
        for task in stuck_tasks:
            cursor.execute("""
                UPDATE tasks
                SET status='failed',
                    progress=0,
                    error_message=?,
                    updated_at=?
                WHERE task_id=?
            """, (
                f"Task abandoned - stuck in {task['status']} for >{max_hours} hours",
                now.isoformat().replace('+00:00', 'Z'),
                task['task_id']
            ))
            cleanup_stats["marked_failed"] += 1
            cleanup_stats["failed_tasks"].append({
                "task_id": task["task_id"],
                "task_type": task["task_type"],
                "hours_stuck": task["hours_stuck"]
            })

        conn.commit()
        conn.close()

    except Exception as e:
        cleanup_stats["error"] = str(e)

    return cleanup_stats

