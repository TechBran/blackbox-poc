def generate_embedding(text: str, max_retries: int = 3) -> Optional[List[float]]:
    """Generate embedding for text using Gemini Embedding API.

    Returns None if embedding generation fails after retries.
    """
    if not GOOGLE_API_KEY:
        print("[EMBEDDING] Warning: GOOGLE_API_KEY not configured, skipping embedding generation")
        return None

    # Truncate text if too long (Gemini has token limits)
    max_chars = 10000
    if len(text) > max_chars:
        text = text[:max_chars] + "..."

    for attempt in range(max_retries):
        try:
            result = genai.embed_content(
                model="models/gemini-embedding-001",
                content=text,
                task_type="retrieval_document"
            )
            return result['embedding']
        except Exception as e:
            print(f"[EMBEDDING] Attempt {attempt + 1}/{max_retries} failed: {e}")
            if attempt < max_retries - 1:
                time.sleep(1)  # Brief delay before retry
            else:
                print(f"[EMBEDDING] Failed to generate embedding after {max_retries} attempts")
                return None

def cosine_similarity(vec1: List[float], vec2: List[float]) -> float:
    """Calculate cosine similarity between two vectors."""
    if not vec1 or not vec2 or len(vec1) != len(vec2):
        return 0.0

    dot_product = sum(a * b for a, b in zip(vec1, vec2))
    magnitude1 = math.sqrt(sum(a * a for a in vec1))
    magnitude2 = math.sqrt(sum(b * b for b in vec2))

    if magnitude1 == 0 or magnitude2 == 0:
        return 0.0

    return dot_product / (magnitude1 * magnitude2)

def semantic_search(query: str, operator: str = "", k: int = 10) -> List[Tuple[str, float]]:
    """Perform semantic search using embeddings.

    Returns list of (snapshot_id, similarity_score) tuples, sorted by relevance.

    Special case: "system" operator can see ALL snapshots from all operators.
    This is needed for outbound calls where the AI needs context from prior conversations.
    """
    # Generate query embedding
    query_embedding = generate_embedding(query)
    if not query_embedding:
        print("[SEMANTIC] Query embedding failed, falling back to keyword search")
        return []

    # Load snapshot index
    index = load_snapshot_index()
    if not index:
        return []

    # Calculate similarity with all snapshots
    scores = []
    for snap_id, data in index.items():
        # Filter by operator if specified - "system" sees all operators
        if operator and operator != "system" and data.get("operator") != operator:
            continue

        # Skip if no embedding stored
        if "embedding" not in data or not data["embedding"]:
            continue

        # Calculate similarity
        similarity = cosine_similarity(query_embedding, data["embedding"])
        scores.append((snap_id, similarity))

    # Sort by similarity (highest first) and return top k
    scores.sort(key=lambda x: x[1], reverse=True)
    return scores[:k]

