def cap_chars(text: str, max_chars: int) -> str:
    if len(text) <= max_chars:
        return text
    lines = text.splitlines()
    if not lines:
        return text[:max_chars]
    start_line = lines[0]
    end_line = lines[-1] if lines[-1].startswith("=== END SNAPSHOT") else ""
    body_lines = lines[1:-1] if end_line else lines[1:]
    body = "\n".join(body_lines)
    budget = max_chars - len(start_line) - len(end_line) - 8
    if budget <= 0:
        return start_line[:max_chars]
    trimmed = body[:budget]
    parts = [start_line, trimmed]
    if end_line: parts.append(end_line)
    return "\n".join(parts)

PLAN_LINE_RX = re.compile(r'^\s*(?:\d+[\.\)]|\-|\*|•)\s+', re.M)
def extract_plan(text: str) -> str:
    """Return only the bullet/numbered list portion if present, else ''."""
    lines = text.splitlines()
    plan_lines = [ln for ln in lines if PLAN_LINE_RX.search(ln)]
    if not plan_lines:
        return ""
    start = next(i for i, ln in enumerate(lines) if PLAN_LINE_RX.search(ln))
    return "\n".join(lines[start:]).strip()

def _extract_clean_text(raw):
    """Fallback text extractor for when full JSON parsing fails.

    Strips markdown code fences and tries to present clean content.
    """
    if not isinstance(raw, str):
        return str(raw)

    text = raw.strip()

    # Strip markdown code fences (```json ... ``` or ``` ... ```)
    if text.startswith("```"):
        # Find the end of the opening fence line
        first_newline = text.find('\n')
        if first_newline != -1:
            text = text[first_newline + 1:]

    if text.endswith("```"):
        text = text[:-3]

    # Strip any remaining backticks at edges
    text = text.strip().strip('`').strip()

    # If it looks like truncated JSON, try to extract meaningful content
    if text.startswith('{') and '"ui_reply"' in text:
        # This is likely a truncated JSON response
        # Try to find content after "ui_reply": "
        idx = text.find('"ui_reply"')
        if idx != -1:
            # Find the start of the value
            colon_idx = text.find(':', idx)
            if colon_idx != -1:
                # Skip whitespace and opening quote
                val_start = colon_idx + 1
                while val_start < len(text) and text[val_start] in ' \t\n\r"':
                    val_start += 1
                # Return everything after, even if truncated
                remaining = text[val_start:]
                if remaining:
                    # Clean up any trailing partial JSON
                    # Remove trailing incomplete escape sequences or quotes
                    while remaining and remaining[-1] in '\\"':
                        remaining = remaining[:-1]
                    return remaining.strip()

    return text


