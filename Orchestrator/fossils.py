def split_snapshots(vol_txt: str) -> List[Tuple[str, int, int]]:
    snaps = []
    pos = 0
    while True:
        m_start = START_RX.search(vol_txt, pos)
        if not m_start:
            break
        m_end = END_RX.search(vol_txt, m_start.end())
        if not m_end:
            break
        s = m_start.start()
        e = m_end.end()
        snaps.append((vol_txt[s:e], s, e))
        pos = e
    return snaps

def extract_snap_ids(blocks: List[str]) -> List[str]:
    """Extract snapshot IDs from snapshot text blocks.

    IMPORTANT: Must extract from START marker, not just any SNAP-ID occurrence!
    Snapshots contain provenance references to other snapshots, so a simple
    SNAP_ID_RX.search() will find the wrong ID (from provenance, not header).
    """
    out: List[str] = []
    for b in blocks or []:
        # Extract from START marker specifically
        m = START_RX.search(b)
        if m:
            out.append(m.group("snap"))
        else:
            # Fallback to simple pattern if no START marker (shouldn't happen)
            m = SNAP_ID_RX.search(b)
            if m:
                out.append(m.group(1))
    return out

def get_recent_fossils_for_operator(vol_txt: str, op: str, count: int, cap_chars_each: int) -> List[str]:
    """Get recent snapshots for an operator. Uses index if available for O(1) access.

    IMPORTANT: Byte offsets in the index are BYTE positions, not character positions.
    We must read the file as bytes, slice using byte offsets, then decode individual snapshots.

    Special case: "system" operator can see ALL snapshots from all operators.
    This is needed for outbound calls where the AI needs context from prior conversations.
    """
    index = load_snapshot_index()

    if index:
        # Use index for fast retrieval - MUST use bytes for byte-offset slicing
        # Read file as bytes to allow proper byte-offset indexing
        vol_bytes = read_volume_bytes(VOL_PATH)

        # Filter by operator - "system" sees all operators
        if op == "system":
            matching = [(snap_id, meta) for snap_id, meta in index.items()]
        else:
            matching = [(snap_id, meta) for snap_id, meta in index.items() if meta.get("operator") == op]

        # Sort by snap_id with proper numeric comparison on date and sequence
        def snapshot_sort_key(snap_id: str) -> tuple:
            """Parse SNAP-YYYYMMDD-NNN and return (date_int, seq_int) for proper sorting."""
            try:
                parts = snap_id.split('-')
                date_part = int(parts[1]) if len(parts) > 1 else 0
                seq_part = int(parts[2]) if len(parts) > 2 else 0
                return (date_part, seq_part)
            except (ValueError, IndexError):
                # Fallback for malformed IDs: sort them at the beginning
                return (0, 0)

        matching.sort(key=lambda x: snapshot_sort_key(x[0]))

        # Validate index consistency
        if matching:
            max_byte_end = max(meta["byte_end"] for _, meta in matching)
            if max_byte_end > len(vol_bytes):
                print(f"[WARNING] Index expects file to be at least {max_byte_end} bytes, but vol_bytes is only {len(vol_bytes)} bytes")

        # Get all snapshots (no limit)
        recent = matching[:]
        texts = []

        for snap_id, meta in recent:
            start = meta["byte_start"]
            end = meta["byte_end"]
            # Direct byte-range read using BYTE offsets on BYTES
            if start < len(vol_bytes) and end <= len(vol_bytes):
                snap_bytes = vol_bytes[start:end]
                # Decode the snapshot bytes to text
                snap_text = snap_bytes.decode('utf-8', errors='replace')

                # Verify snapshot integrity
                m = START_RX.search(snap_text)
                if m:
                    extracted_id = m.group("snap")
                    if extracted_id != snap_id:
                        print(f"[ERROR] Index mismatch! Expected {snap_id} at bytes {start}-{end}, but got {extracted_id}")
                else:
                    print(f"[ERROR] No START marker found for {snap_id} at bytes {start}-{end}")

            elif start < len(vol_bytes):
                # Partial snapshot available
                snap_bytes = vol_bytes[start:]
                snap_text = snap_bytes.decode('utf-8', errors='replace')
                print(f"[WARNING] Snapshot {snap_id} byte_end ({end}) exceeds vol_bytes length ({len(vol_bytes)}), text may be incomplete")
            else:
                snap_text = ""
                print(f"[WARNING] Snapshot {snap_id} byte_start ({start}) exceeds vol_bytes length ({len(vol_bytes)})")

            texts.append(cap_chars(snap_text, cap_chars_each))

        final_texts = texts[-count:] if count > 0 else texts
        return final_texts
    else:
        # Fallback to old method if index not available
        snaps = split_snapshots(vol_txt)
        texts = [t for (t, _s, _e) in snaps[:]]  # Get all snapshots (no limit)
        texts = [cap_chars(t, cap_chars_each) for t in texts]
        texts = filter_by_operator(texts, op)
        return texts[-count:] if count>0 else texts

def _simple_stem(word: str) -> str:
    """Improved suffix-based stemming for common English patterns.

    More conservative approach that avoids over-stemming and creating odd stems.
    """
    # Don't stem very short words (< 4 chars)
    if len(word) < 4:
        return word

    # Protected words that should never be stemmed
    protected = {
        'snapshot', 'checkpoint', 'error', 'index', 'bytes', 'fossil',
        'endpoint', 'database', 'async', 'status', 'process'
    }
    if word in protected:
        return word

    # Handle suffixes in order of specificity (longest first to avoid conflicts)
    # Only stem if the root will be meaningful (>= 4 chars)
    suffixes = [
        ('ication', 'ify'),   # modification → modify
        ('ational', 'ate'),   #ational → nate
        ('ization', 'ize'),   # realization → realize
        ('ousness', 'ous'),   # consciousness → conscious
        ('fulness', 'ful'),   # usefulness → useful
        ('iveness', 'ive'),   # effectiveness → effective
        ('ation', 'ate'),     # creation → create
        ('ition', 'it'),      # condition → condit (but protected above)
        ('ement', ''),        # replacement → replac
        ('ment', ''),         # deployment → deploy
        ('ness', ''),         #ingness → ing
        ('ity', ''),          # complexity → complex
        ('ful', ''),          # successful → success
        ('less', ''),         # endless → end
        ('ing', ''),          # indexing → index
        ('ed', ''),           # indexed → index
        ('es', ''),           # indexes → index
        ('s', ''),            # snapshots → snapshot (but be careful)
    ]

    for suffix, replacement in suffixes:
        if word.endswith(suffix) and len(word) > len(suffix) + 2:
            # Calculate what the stem would be
            stem_base = word[:-len(suffix)]
            stemmed = stem_base + replacement

            # Only apply stemming if:
            # 1. The stem is at least 3 characters
            # 2. For 's' suffix, only remove if the word is plural (ends in 's' but not 'ss', 'us', 'is')
            if suffix == 's':
                # Don't remove 's' from: class, bus, this, analysis, etc.
                if word.endswith(('ss', 'us', 'is', 'as', 'os')) or len(stem_base) < 3:
                    continue

            if len(stemmed) >= 3:
                return stemmed

    return word

def _extract_terms(query: str) -> List[str]:
    """Extract meaningful terms from query, filtering stop words and applying stemming."""
    # Use word boundary regex to preserve complete words (including apostrophes and hyphens)
    # This prevents "don't" from becoming ["don", "t"]
    words = [w.lower() for w in re.findall(r"\b[a-zA-Z][a-zA-Z0-9''\-]*[a-zA-Z0-9]\b|\b[a-zA-Z]\b", query or "")]

    # Filter out stop words and very short words (< 3 chars after removing punctuation)
    filtered = []
    for w in words:
        # Remove trailing/leading apostrophes and hyphens for comparison
        clean_word = w.strip("'-'")

        # Skip if it's a stop word or too short
        if clean_word not in STOP_WORDS and len(clean_word) >= 3:
            filtered.append(clean_word)

    # Apply simple stemming to normalize terms
    stemmed = [_simple_stem(w) for w in filtered]

    # Remove duplicates while preserving order
    seen = set()
    unique_terms = []
    for term in stemmed:
        if term not in seen and term not in STOP_WORDS:
            seen.add(term)
            unique_terms.append(term)

    return unique_terms

def _generate_ngrams(query: str, n: int = 2) -> List[str]:
    """Generate n-grams (phrases) from the query for exact phrase matching.

    Filters out n-grams that consist entirely of stop words or contain mostly stop words.
    """
    # Extract words using the same regex as _extract_terms to be consistent
    words = [w.lower() for w in re.findall(r"\b[a-zA-Z][a-zA-Z0-9''\-]*[a-zA-Z0-9]\b|\b[a-zA-Z]\b", query or "")]

    ngrams = []
    for i in range(len(words) - n + 1):
        phrase_words = words[i:i+n]
        phrase = " ".join(phrase_words)

        # Clean words for stop word checking
        clean_words = [w.strip("'-'") for w in phrase_words]

        # Count non-stop words in the phrase
        non_stop_count = sum(1 for w in clean_words if w not in STOP_WORDS and len(w) >= 3)

        # Only keep phrases that:
        # 1. Have at least one meaningful (non-stop) word
        # 2. Don't consist entirely of stop words
        # 3. Have reasonable length (>= 6 characters)
        if non_stop_count >= 1 and len(phrase) >= 6:
            # Further filter: for bigrams, require at least 1 non-stop word
            # For trigrams, require at least 2 non-stop words
            if (n == 2 and non_stop_count >= 1) or (n >= 3 and non_stop_count >= 2):
                ngrams.append(phrase)

    return ngrams

def _compute_tfidf_scores(texts: List[str], terms: List[str]) -> List[float]:
    """Compute TF-IDF scores for each text based on query terms."""
    if not terms:
        return [1.0] * len(texts)
    
    # Document frequency: how many documents contain each term
    df = {}
    for term in terms:
        df[term] = sum(1 for text in texts if term in text.lower())
    
    # Compute TF-IDF score for each document
    scores = []
    num_docs = len(texts)
    for text in texts:
        lc = text.lower()
        score = 0.0
        for term in terms:
            tf = lc.count(term)  # Term frequency in this document
            if tf > 0 and df[term] > 0:
                idf = math.log(num_docs / df[term])  # Inverse document frequency
                score += tf * idf
        scores.append(score)
    return scores

def keyword_retrieve(vol_txt: str, query: str, k: int = 3) -> List[str]:
    """Enhanced keyword retrieval with stop-word filtering, TF-IDF, and n-gram matching."""
    terms = _extract_terms(query)
    bigrams = _generate_ngrams(query, 2)
    trigrams = _generate_ngrams(query, 3)
    
    if not terms and not bigrams and not trigrams:
        return []

    snaps = split_snapshots(vol_txt)
    texts = [text for (text, _s, _e) in snaps[:]]  # Get all snapshots (no limit)
    
    # Compute TF-IDF scores
    tfidf_scores = _compute_tfidf_scores(texts, terms)
    
    # Score each snapshot
    scored = []
    for i, text in enumerate(texts):
        lc = text.lower()
        
        # Base TF-IDF score
        score = tfidf_scores[i]
        
        # Boost for exact phrase matches (n-grams)
        for bigram in bigrams:
            if bigram in lc:
                score += 5.0  # Significant boost for 2-word phrase match
        
        for trigram in trigrams:
            if trigram in lc:
                score += 10.0  # Even larger boost for 3-word phrase match
        
        if score > 0:
            scored.append((score, text))
    
    scored.sort(key=lambda x: x[0], reverse=True)
    return [t for _, t in scored[:k]]

def keyword_retrieve_for_operator(vol_txt: str, query: str, k: int, op: str) -> List[str]:
    """Operator-aware wrapper for enhanced keyword retrieval. Uses index for O(1) operator filtering.

    Special case: "system" operator can see ALL snapshots from all operators.
    """
    index = load_snapshot_index()

    if index:
        # Use index to pre-filter by operator - "system" sees all
        if op == "system":
            matching = [(snap_id, meta) for snap_id, meta in index.items()]
        else:
            matching = [(snap_id, meta) for snap_id, meta in index.items() if meta.get("operator") == op]
        matching.sort(key=lambda x: x[0])
        recent = matching[:]  # Get all snapshots (no limit)

        # FIX 1: Read as BYTES to use byte offsets correctly (not string slicing)
        vol_bytes = read_volume_bytes(VOL_PATH)

        # Extract texts using byte offsets
        texts = []
        for snap_id, meta in recent:
            start = meta["byte_start"]
            end = meta["byte_end"]
            # Use byte-based extraction instead of string slicing
            if start < len(vol_bytes) and end <= len(vol_bytes):
                snap_bytes = vol_bytes[start:end]
                snap_text = snap_bytes.decode('utf-8', errors='replace')
            else:
                snap_text = ""
            texts.append(snap_text)

        # Now run keyword matching on pre-filtered snapshots
        terms = _extract_terms(query)
        bigrams = _generate_ngrams(query, 2)
        trigrams = _generate_ngrams(query, 3)

        if not terms and not bigrams and not trigrams:
            return []

        tfidf_scores = _compute_tfidf_scores(texts, terms)
        scored = []

        num_texts = len(texts)

        for i, text in enumerate(texts):
            lc = text.lower()
            score = tfidf_scores[i]

            # Add technical term boost: if query contains technical terms, boost score
            technical_term_count = sum(1 for term in terms if term in TECHNICAL_TERMS)
            if technical_term_count > 0:
                # Check if this snapshot contains those technical terms
                matched_technical = sum(1 for term in terms if term in TECHNICAL_TERMS and term in lc)
                if matched_technical > 0:
                    technical_boost = (matched_technical / technical_term_count) * 3.0  # Up to +3.0
                    score += technical_boost

            # FIX 2: Add recency bonus - newer snapshots rank higher
            # i=0 is oldest in recent[] list, i=num_texts-1 is newest
            if num_texts > 1:
                recency_factor = i / (num_texts - 1)  # 0.0 to 1.0
                recency_bonus = recency_factor * 2.0  # Up to +2.0 for most recent
                score += recency_bonus

            for bigram in bigrams:
                if bigram in lc:
                    score += 5.0

            for trigram in trigrams:
                if trigram in lc:
                    score += 10.0

            if score > 0:
                scored.append((score, text))

        scored.sort(key=lambda x: x[0], reverse=True)
        base = [t for _, t in scored[:k]]
    else:
        # Fallback to old method if index not available
        base = keyword_retrieve(vol_txt, query, k=999999) if query else []  # Get all results (no limit)
        base = filter_by_operator(base, op)
    
    # Re-score after operator filtering (optional refinement)
    terms = _extract_terms(query)
    bigrams = _generate_ngrams(query, 2)
    trigrams = _generate_ngrams(query, 3)
    
    scored = []
    for t in base:
        lc = t.lower()
        score = sum(lc.count(w) for w in terms) if terms else 1
        
        # Boost for phrases
        for bigram in bigrams:
            if bigram in lc:
                score += 5.0
        for trigram in trigrams:
            if trigram in lc:
                score += 10.0
        
        if score > 0:
            scored.append((score, t))
    
    scored.sort(key=lambda x: x[0], reverse=True)
    return [t for _, t in scored[:k]]

