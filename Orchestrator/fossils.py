def cap_chars(text: str, max_chars: int) -> str:
    """Truncate text to max_chars, preserving snapshot structure."""
    if len(text) <= max_chars:
        return text
    lines = text.splitlines()
    if not lines:
        return text[:max_chars]
    start_line = lines[0]
    end_line = lines[-1] if lines[-1].startswith("=== END SNAPSHOT") else ""
    body_lines = lines[1:-1] if end_line else lines[1:]
    body = "\n".join(body_lines)
    budget = max_chars - len(start_line) - len(end_line) - 8
    if budget <= 0:
        return start_line[:max_chars]
    trimmed = body[:budget]
    parts = [start_line, trimmed]
    if end_line: parts.append(end_line)
    return "\n".join(parts)


def hybrid_retrieve(vol_txt: str, query: str, k: int = 3, operator: str = "") -> List[str]:
    """Hybrid retrieval combining keyword (TF-IDF) + semantic (embeddings) search.

    Combines both approaches for best results:
    - Keyword search: Fast, precise for specific terms
    - Semantic search: Understands intent and context

    Returns top k snapshots ranked by combined score.
    """
    # Get more candidates from each method, then combine
    candidate_k = min(k * 5, 20)  # Get 5x candidates from each (up to 20)

    # 1. Keyword search (TF-IDF + n-grams)
    keyword_results = []
    if operator:
        keyword_results = keyword_retrieve_for_operator(vol_txt, query, candidate_k, operator)
    else:
        keyword_results = keyword_retrieve(vol_txt, query, candidate_k)

    # 2. Semantic search (embeddings) - lazy import to avoid circular dependency
    from Orchestrator.monitoring import semantic_search
    semantic_results = semantic_search(query, operator=operator, k=candidate_k)

    # Create snapshot ID to text mapping for keyword results
    snap_to_text = {}
    index = load_snapshot_index()
    vol_bytes = read_volume_bytes(VOL_PATH)

    for snap_id, meta in index.items():
        # Filter by operator if specified - "system" sees all operators
        if operator and operator != "system" and meta.get("operator") != operator:
            continue

        start = meta["byte_start"]
        end = meta["byte_end"]
        if start < len(vol_bytes) and end <= len(vol_bytes):
            snap_bytes = vol_bytes[start:end]
            snap_text = snap_bytes.decode('utf-8', errors='replace')
            snap_to_text[snap_id] = snap_text

    # Normalize keyword scores (map rank to score)
    keyword_scores = {}
    for rank, text in enumerate(keyword_results):
        # Find snap_id for this text
        for snap_id, snap_text in snap_to_text.items():
            if snap_text == text:
                # Score decreases with rank: 1.0 for first, 0.0 for last
                keyword_scores[snap_id] = 1.0 - (rank / max(len(keyword_results), 1))
                break

    # Normalize semantic scores (already 0-1 from cosine similarity)
    semantic_scores = {snap_id: score for snap_id, score in semantic_results}

    # Combine scores with weights
    KEYWORD_WEIGHT = 0.4   # 40% keyword
    SEMANTIC_WEIGHT = 0.6  # 60% semantic

    combined_scores = {}
    all_snap_ids = set(keyword_scores.keys()) | set(semantic_scores.keys())

    for snap_id in all_snap_ids:
        kw_score = keyword_scores.get(snap_id, 0.0)
        sem_score = semantic_scores.get(snap_id, 0.0)
        combined_scores[snap_id] = (KEYWORD_WEIGHT * kw_score) + (SEMANTIC_WEIGHT * sem_score)

    # Sort by combined score and return top k
    sorted_snaps = sorted(combined_scores.items(), key=lambda x: x[1], reverse=True)[:k]

    # Convert back to text
    results = []
    for snap_id, score in sorted_snaps:
        if snap_id in snap_to_text:
            results.append(snap_to_text[snap_id])

    print(f"[HYBRID] Combined {len(keyword_results)} keyword + {len(semantic_results)} semantic results → {len(results)} final")
    return results


def semantic_retrieve(query: str, operator: str = "", k: int = 15, threshold: float = 0.7) -> List[str]:
    """Pure semantic retrieval with threshold filtering.

    Returns snapshots where semantic similarity >= threshold, up to k results.
    Uses embeddings for meaning-based search, separate from keyword search.

    Args:
        query: Search query text
        operator: Filter by operator (empty string = all operators)
        k: Maximum number of results to return
        threshold: Minimum similarity score (0.0-1.0), default 0.7

    Returns:
        List of snapshot texts, sorted by similarity (highest first)
    """
    from Orchestrator.monitoring import semantic_search

    # Get semantic results with scores
    semantic_results = semantic_search(query, operator=operator, k=k * 2)  # Get extra candidates for threshold filtering

    # Filter by threshold
    filtered_results = [(snap_id, score) for snap_id, score in semantic_results if score >= threshold]

    # Limit to k results
    filtered_results = filtered_results[:k]

    if not filtered_results:
        print(f"[SEMANTIC] No results above threshold {threshold} (had {len(semantic_results)} candidates)")
        return []

    # Load index and volume for text retrieval
    index = load_snapshot_index()
    vol_bytes = read_volume_bytes(VOL_PATH)

    # Convert snap_ids to text
    results = []
    for snap_id, score in filtered_results:
        if snap_id in index:
            meta = index[snap_id]
            start = meta["byte_start"]
            end = meta["byte_end"]

            if start < len(vol_bytes) and end <= len(vol_bytes):
                snap_bytes = vol_bytes[start:end]
                snap_text = snap_bytes.decode('utf-8', errors='replace')
                results.append(snap_text)

    print(f"[SEMANTIC] Retrieved {len(results)} snapshots above threshold {threshold} (scores: {[f'{s:.3f}' for _, s in filtered_results[:5]]}{'...' if len(filtered_results) > 5 else ''})")
    return results


