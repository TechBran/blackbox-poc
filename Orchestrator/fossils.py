# In-memory index cache — avoids re-parsing 254MB JSON on every call
_index_cache: Optional[Dict[str, Dict[str, Any]]] = None
_index_cache_mtime: float = 0.0


def load_snapshot_index() -> Dict[str, Dict[str, Any]]:
    """Load the snapshot index, using in-memory cache when possible.

    The cache is invalidated when the file's mtime changes (i.e., after save).
    This avoids parsing 254MB of JSON on every search/mint/status call.
    """
    global _index_cache, _index_cache_mtime
    try:
        if not SNAPSHOT_INDEX.exists():
            return {}
        current_mtime = SNAPSHOT_INDEX.stat().st_mtime
        if _index_cache is not None and current_mtime == _index_cache_mtime:
            return _index_cache
        with open(SNAPSHOT_INDEX, 'r', encoding='utf-8') as f:
            _index_cache = json.load(f)
        _index_cache_mtime = current_mtime
        return _index_cache
    except Exception as e:
        print(f"[WARNING] Failed to load snapshot index: {e}")
    return {}


def get_snapshot_by_id(snap_id: str) -> Optional[Dict[str, Any]]:
    """
    Retrieve a specific snapshot by its ID.

    Args:
        snap_id: Snapshot ID (e.g., "SNAP-20251207-1234")

    Returns:
        Dict with 'content', 'metadata' (operator, timestamp, type), or None if not found
    """
    index = load_snapshot_index()

    if snap_id not in index:
        return None

    meta = index[snap_id]
    vol_bytes = read_volume_bytes(VOL_PATH)

    start = meta.get("byte_start", 0)
    end = meta.get("byte_end", 0)

    if start >= len(vol_bytes) or end > len(vol_bytes):
        return None

    snap_bytes = vol_bytes[start:end]
    content = snap_bytes.decode('utf-8', errors='replace')

    return {
        "snap_id": snap_id,
        "content": content,
        "metadata": {
            "operator": meta.get("operator"),
            "timestamp": meta.get("timestamp"),
            "type": meta.get("type", "normal"),
            "byte_start": start,
            "byte_end": end
        }
    }


def save_snapshot_index(index: Dict[str, Dict[str, Any]]):
    """Save the snapshot index to disk atomically and update the in-memory cache."""
    global _index_cache, _index_cache_mtime
    try:
        SNAPSHOT_INDEX.parent.mkdir(parents=True, exist_ok=True)
        tmp_path = SNAPSHOT_INDEX.with_suffix('.tmp')
        with open(tmp_path, 'w', encoding='utf-8') as f:
            json.dump(index, f, indent=2)
        os.replace(tmp_path, SNAPSHOT_INDEX)
        # Update cache directly — avoid re-reading the file we just wrote
        _index_cache = index
        _index_cache_mtime = SNAPSHOT_INDEX.stat().st_mtime
    except Exception as e:
        print(f"[ERROR] Failed to save snapshot index: {e}")

def rebuild_snapshot_index() -> Dict[str, Dict[str, Any]]:
    """Rebuild the entire snapshot index by scanning the volume file.

    CRITICAL: Works directly on BYTES using byte-compiled regexes so that
    byte offsets are true file positions — no O(n²) char-to-byte conversion.

    IMPORTANT: Preserves existing embeddings when rebuilding to avoid losing
    expensive embedding data.
    """
    print("[INDEX] Rebuilding snapshot index from volume file...")
    start_time = time.time()

    # Load existing index to preserve embeddings
    old_index = load_snapshot_index()
    embeddings_preserved = 0

    index = {}
    if not VOL_PATH.exists():
        return index

    # Read as BYTES — regex directly on bytes gives true byte offsets (O(n))
    vol_bytes = read_volume_bytes(VOL_PATH)

    # Byte-level regexes for START and END markers
    # START captures snap_id and UTC timestamp (always present and consistent)
    START_RX_B = re.compile(rb'=== START SNAPSHOT \xe2\x80\x94 UTC (?P<utc>\d{4}-\d{2}-\d{2}T[\d:]+Z) \xe2\x80\x94 (?P<snap>SNAP-\d{8}-\d+)')
    # END uses snap_id-specific match to avoid cross-boundary jumps
    OP_RX_B = re.compile(rb'OPERATOR:\s*([^\n]+)')

    pos = 0
    count = 0

    while True:
        m_start = START_RX_B.search(vol_bytes, pos)
        if not m_start:
            break
        snap_id = m_start.group("snap").decode('utf-8')
        utc = m_start.group("utc").decode('utf-8')

        # Search for THIS snapshot's specific END marker
        end_pattern = rb'=== END SNAPSHOT \xe2\x80\x94 ' + re.escape(snap_id.encode()) + rb'.*?==='
        m_end = re.search(end_pattern, vol_bytes[m_start.end():m_start.end() + 200000])
        if not m_end:
            # Skip this snapshot — no matching END found
            pos = m_start.end()
            continue

        # Byte positions — m_end is relative to the slice, convert to absolute
        byte_start = m_start.start()
        byte_end = m_start.end() + m_end.end()

        # Extract operator from snapshot bytes
        snap_bytes = vol_bytes[byte_start:byte_end]
        operator = "Unknown"
        op_match = OP_RX_B.search(snap_bytes)
        if op_match:
            operator = op_match.group(1).strip().decode('utf-8', errors='replace')

        # Detect if this is a checkpoint snapshot
        snap_type = "checkpoint" if b"[CHECKPOINT]" in snap_bytes else "normal"

        index[snap_id] = {
            "byte_start": byte_start,
            "byte_end": byte_end,
            "operator": operator,
            "timestamp": utc,
            "type": snap_type
        }

        # Preserve embedding from old index if it exists
        if snap_id in old_index and "embedding" in old_index[snap_id]:
            index[snap_id]["embedding"] = old_index[snap_id]["embedding"]
            embeddings_preserved += 1

        pos = byte_end
        count += 1

        if count % 500 == 0:
            print(f"[INDEX] Processed {count} snapshots...")

    save_snapshot_index(index)
    elapsed = time.time() - start_time
    print(f"[INDEX] Rebuilt index with {count} snapshots in {elapsed:.2f}s")
    if embeddings_preserved > 0:
        print(f"[INDEX] Preserved {embeddings_preserved} embeddings from previous index")
    return index

