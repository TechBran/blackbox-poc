@app.post("/tts/batch")
async def tts_batch(body: dict = Body(...)):
    """Batch TTS: split text into chunks, generate all in parallel, return combined audio.

    Accepts:
        text (str): Full text to synthesize
        provider (str): "openai", "gemini-pro", or "gemini-flash" (default: "openai")
        voice (str): Voice name (default: config TTS_VOICE)
        model (str): Model override (provider-specific)
        format (str): "mp3" or "wav" (default: config TTS_FORMAT)
        operator (str): Operator name for logging
        max_chunk_chars (int): Max characters per chunk (default: 800)
    """
    t_start = time.time()

    text = (body.get("text") or "").strip()
    if not text:
        raise HTTPException(400, "No text provided")

    provider = (body.get("provider") or "openai").strip().lower()
    voice = (body.get("voice") or TTS_VOICE).strip()
    model = (body.get("model") or "").strip()
    audio_format = (body.get("format") or TTS_FORMAT).strip()
    operator = (body.get("operator") or "unknown").strip()
    # Provider-specific chunk defaults: OpenAI is fast per-request, Gemini benefits from smaller chunks
    default_chunk = 1500 if provider == "openai" else 800
    max_chunk_chars = int(body.get("max_chunk_chars", default_chunk))

    # Split text into chunks
    chunks = chunk_text_for_tts(text, max_chars=max_chunk_chars)
    if not chunks:
        raise HTTPException(400, "Text produced no chunks after splitting")

    num_chunks = len(chunks)
    print(f"[TTS Batch] {provider} | {num_chunks} chunks | voice={voice} | format={audio_format} | operator={operator}")

    loop = asyncio.get_running_loop()

    # --- OpenAI provider ---
    if provider == "openai":
        api_key = OPENAI_API_KEY.strip()
        if not api_key:
            raise HTTPException(400, "OPENAI_API_KEY not configured")

        effective_model = model or TTS_MODEL

        def _openai_tts_chunk(chunk_text: str) -> bytes:
            req = {
                "model": effective_model,
                "input": chunk_text,
                "voice": voice,
                "response_format": audio_format,
            }
            r = requests.post(
                OPENAI_TTS_URL,
                json=req,
                headers={"Authorization": f"Bearer {api_key}"},
                timeout=TTS_TIMEOUT / 1000.0,
            )
            if r.status_code != 200:
                raise HTTPException(502, f"OpenAI TTS failed (HTTP {r.status_code}): {r.text[:200]}")
            return r.content

        # Run all chunks in parallel
        futures = [loop.run_in_executor(_tts_executor, _openai_tts_chunk, chunk) for chunk in chunks]
        results = await asyncio.gather(*futures, return_exceptions=True)

    # --- Gemini provider ---
    elif provider in ("gemini-pro", "gemini-flash"):
        def _gemini_tts_chunk(chunk_text: str) -> bytes:
            inp = GeminiProTTSIn(
                text=chunk_text,
                voice_name=voice,
                model=model if model else None,
                operator=operator,
            )
            # call_gemini_tts returns WAV bytes
            return call_gemini_tts(inp)

        futures = [loop.run_in_executor(_tts_executor, _gemini_tts_chunk, chunk) for chunk in chunks]
        results = await asyncio.gather(*futures, return_exceptions=True)

        # Gemini always returns WAV; override format for stitching logic
        audio_format = "wav"

    else:
        raise HTTPException(400, f"Unknown provider: {provider}. Use 'openai', 'gemini-pro', or 'gemini-flash'.")

    # Check for errors in results
    errors = [(i, r) for i, r in enumerate(results) if isinstance(r, Exception)]
    if errors:
        err_idx, err = errors[0]
        print(f"[TTS Batch] ERROR on chunk {err_idx}/{num_chunks}: {err}")
        raise HTTPException(502, f"TTS failed on chunk {err_idx + 1}/{num_chunks}: {err}")

    audio_chunks: List[bytes] = list(results)  # type: ignore[arg-type]

    # Stitch results
    if audio_format == "wav":
        combined = stitch_wav_chunks(audio_chunks)
    else:
        # MP3/opus: simple byte concatenation (frames are self-delimiting)
        combined = b"".join(audio_chunks)

    elapsed = time.time() - t_start
    mime = "audio/mpeg" if audio_format == "mp3" else ("audio/ogg" if audio_format == "opus" else "audio/wav")
    print(f"[TTS Batch] Done: {num_chunks} chunks, {len(combined)} bytes, {elapsed:.2f}s | {mime}")

    return StreamingResponse(iter([combined]), media_type=mime)


@app.post("/tts/stitch")
async def tts_stitch(chunks: List[UploadFile] = File(...)):
    """Stitch multiple WAV file chunks into a single combined WAV file.

    Accepts multipart form data with one or more WAV file uploads.
    Uses proper WAV header/frame stitching to avoid audio artifacts.
    """
    if not chunks:
        raise HTTPException(400, "No files provided")

    wav_chunks: List[bytes] = []
    for f in chunks:
        data = await f.read()
        if not data:
            continue
        wav_chunks.append(data)

    if not wav_chunks:
        raise HTTPException(400, "All uploaded files were empty")

    print(f"[TTS Stitch] Stitching {len(wav_chunks)} WAV chunks")
    combined = stitch_wav_chunks(wav_chunks)
    print(f"[TTS Stitch] Result: {len(combined)} bytes")

    return StreamingResponse(iter([combined]), media_type="audio/wav")


