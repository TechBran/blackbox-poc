def call_imagen(prompt: str, model: str, options: dict = None) -> list:
    """
    Calls Gemini Image Generation API (Nano Banana Pro) and returns image bytes.
    Uses generateContent endpoint with IMAGE response modality.

    Args:
        prompt: Text prompt for image generation
        model: Model to use (e.g., gemini-2.0-flash-exp-image-generation)
        options: Optional dict with:
            - aspectRatio: "1:1", "16:9", "9:16", etc. (default "16:9")
            - resolution: "1K", "2K", "4K" (default "1K")
            - numberOfImages: 1-4 (default 1)
            - referenceImages: List of {data, mimeType} for style guidance

    Returns:
        List of image bytes (one per generated image)
    """
    if not GOOGLE_API_KEY:
        raise HTTPException(status_code=400, detail="GOOGLE_API_KEY not configured for image generation.")

    options = options or {}
    aspect_ratio = options.get("aspectRatio", "16:9")
    resolution = options.get("resolution", "1K")
    num_images = min(max(int(options.get("numberOfImages", 1)), 1), 4)  # Clamp 1-4
    reference_images = options.get("referenceImages", [])

    # Build content parts - reference images FIRST, then text prompt
    # This follows the multimodal conversation pattern where images come before the question
    parts = []

    # Add reference images if provided (up to 14) - images come first
    for ref_img in reference_images[:14]:
        if ref_img.get("data") and ref_img.get("mimeType"):
            parts.append({
                "inlineData": {
                    "mimeType": ref_img["mimeType"],
                    "data": ref_img["data"]
                }
            })

    # Add text prompt after images
    parts.append({"text": prompt})

    url = f"{GEMINI_BASE_URL}/{model}:generateContent?key={GOOGLE_API_KEY}"

    # Build generation config with Nano Banana Pro options
    # Note: responseModalities tells Gemini to return images
    # Image options go in imageConfig, not directly in generationConfig
    generation_config = {
        "responseModalities": ["TEXT", "IMAGE"],
        "imageConfig": {
            "aspectRatio": aspect_ratio,
            "imageSize": resolution  # 1K, 2K, or 4K
        }
    }

    payload = {
        "contents": [{"parts": parts}],
        "generationConfig": generation_config
    }

    try:
        print(f"[IMAGE GEN] Using model: {model}")
        print(f"[IMAGE GEN] Prompt: {prompt[:100]}...")
        print(f"[IMAGE GEN] Options: aspect={aspect_ratio}, resolution={resolution}, count={num_images}, refs={len(reference_images)}")

        images = []

        # Generate requested number of images
        for i in range(num_images):
            print(f"[IMAGE GEN] Generating image {i+1}/{num_images}...")
            response = requests.post(url, json=payload, timeout=400)

            # Log response details for debugging
            if response.status_code != 200:
                print(f"[IMAGE GEN] API Error {response.status_code}: {response.text[:500]}")

            response.raise_for_status()
            data = response.json()

            # Parse the Gemini generateContent response format
            if "candidates" in data and data["candidates"]:
                resp_parts = data["candidates"][0].get("content", {}).get("parts", [])
                for part in resp_parts:
                    if "inlineData" in part:
                        b64_string = part["inlineData"].get("data")
                        if b64_string:
                            print(f"[IMAGE GEN] Got image {i+1}")
                            images.append(base64.b64decode(b64_string))
                            break

            # Check for safety blocks
            if "promptFeedback" in data:
                feedback = data["promptFeedback"]
                if feedback.get("blockReason"):
                    raise HTTPException(status_code=400, detail=f"Image generation blocked: {feedback.get('blockReason')}")

        if not images:
            raise HTTPException(status_code=500, detail=f"No images in response from Gemini API: {data}")

        print(f"[IMAGE GEN] Success! Generated {len(images)} image(s)")
        return images

    except requests.exceptions.HTTPError as e:
        # Log the actual error response from API
        error_detail = ""
        try:
            error_detail = e.response.text[:1000] if e.response else str(e)
        except:
            error_detail = str(e)
        print(f"[IMAGE GEN] HTTP Error: {error_detail}")
        raise HTTPException(status_code=502, detail=f"Gemini Image API request failed: {error_detail}")
    except requests.exceptions.RequestException as e:
        print(f"[IMAGE GEN] Request error: {e}")
        raise HTTPException(status_code=502, detail=f"Gemini Image API request failed: {e}")

