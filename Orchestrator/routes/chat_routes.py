def _last_user_msg(messages) -> str:
    """Extract text from the last user message (for ToolVault prompt injection)."""
    for m in reversed(messages if isinstance(messages, list) else []):
        if m.get("role") == "user":
            content = m.get("content", "")
            if isinstance(content, str):
                return content[:500]
            if isinstance(content, list):
                return " ".join(
                    p.get("text", "") for p in content
                    if isinstance(p, dict) and "text" in p
                )[:500]
    return ""


# Cache the last injection result so _get_tools and _get_system_prompt
# can share a single search pass within the same request.
_inject_cache = {"prompt": "", "provider": "", "group": "", "tools": None, "instructions": "", "tool_names": set()}


def _extract_tool_names(tools: list) -> set:
    """Extract tool names from a provider-formatted tools list.

    Handles all four formats:
      Anthropic: [{"name": "x", ...}]
      OpenAI/xAI: [{"type": "function", "function": {"name": "x", ...}}]
      Gemini: [{"functionDeclarations": [{"name": "x", ...}]}]
    """
    names = set()
    if not tools:
        return names
    for t in tools:
        if not isinstance(t, dict):
            continue
        # Anthropic
        if "name" in t and isinstance(t["name"], str):
            names.add(t["name"])
        # OpenAI / xAI
        if t.get("type") == "function" and isinstance(t.get("function"), dict):
            n = t["function"].get("name")
            if isinstance(n, str):
                names.add(n)
        # Gemini
        if "functionDeclarations" in t and isinstance(t["functionDeclarations"], list):
            for fd in t["functionDeclarations"]:
                if isinstance(fd, dict):
                    n = fd.get("name")
                    if isinstance(n, str):
                        names.add(n)
    return names


def _get_tools(provider: str, prompt: str = "", group: str = "chat") -> list:
    """Get tools for an API call — dynamic (ToolVault) or static (legacy).

    When TOOLVAULT_ENABLED=true, uses semantic search to inject only
    the tools relevant to this specific prompt. Also caches the tool
    instructions for _get_system_prompt() to pick up.
    """
    if TOOLVAULT_ENABLED and prompt.strip():
        from Orchestrator.toolvault.injector import inject_for_prompt
        tools, instructions = inject_for_prompt(prompt, provider, group)
        # Cache for _get_system_prompt to use
        _inject_cache["prompt"] = prompt
        _inject_cache["provider"] = provider
        _inject_cache["group"] = group
        _inject_cache["tools"] = tools
        _inject_cache["instructions"] = instructions
        _inject_cache["tool_names"] = _extract_tool_names(tools)
        return tools

    # Static fallback
    _inject_cache["instructions"] = ""
    if provider == "anthropic":
        tools = CHAT_TOOLS_ANTHROPIC_CU if group == "chat_cu" else CHAT_TOOLS_ANTHROPIC
    elif provider in ("openai", "openai_rest", "xai", "grok"):
        tools = CHAT_TOOLS_OPENAI
    elif provider in ("gemini", "gemini_rest", "google"):
        tools = CHAT_TOOLS_GEMINI
    else:
        tools = CHAT_TOOLS_OPENAI
    _inject_cache["tool_names"] = _extract_tool_names(tools)
    return tools


def _get_system_prompt(base_prompt: str = "") -> str:
    """Get the system prompt with dynamic or static tool instructions.

    When TOOLVAULT_ENABLED and _get_tools() was called with a prompt,
    replaces the {TOOL_INSTRUCTIONS} placeholder with dynamically
    generated instructions from the vault. Otherwise returns the
    static OUTPUT_SPEC.

    Args:
        base_prompt: Additional system prompt to prepend (optional).
                     The OUTPUT_SPEC is always included.
    """
    from Orchestrator.config import OUTPUT_SPEC, build_output_spec

    instructions = _inject_cache.get("instructions", "")
    spec = build_output_spec(instructions) if instructions else OUTPUT_SPEC

    if base_prompt:
        return base_prompt + "\n\n" + spec
    return spec

def call_openai(messages: List[Dict], model: str, operator: str = "Brandon"):
    if not OPENAI_API_KEY: raise HTTPException(400, "OPENAI_API_KEY not set")

    # Process messages to handle images, videos, audio, and documents
    processed_messages = []
    for m in messages:
        if m.get("role") in ("user", "assistant", "system"):
            content = m.get("content")
            if isinstance(content, list):
                new_content = []
                for part in content:
                    # Handle direct base64 images (from Android app - Anthropic format)
                    if part.get("type") == "image" and part.get("source", {}).get("type") == "base64":
                        source = part.get("source", {})
                        media_type = source.get("media_type", "image/png")
                        data = source.get("data", "")
                        if data:
                            print(f"[OPENAI] Processing direct base64 image ({len(data)} chars, {media_type})")
                            # OpenAI format: data URL
                            new_content.append({
                                "type": "image_url",
                                "image_url": {
                                    "url": f"data:{media_type};base64,{data}"
                                }
                            })
                    elif part.get("type") == "image_url":
                        # Handle images - convert local paths to base64 data URLs
                        url = part["image_url"]["url"]
                        try:
                            relative_path = url.split("/ui/", 1)[-1]
                            local_path = Path("Portal") / relative_path
                            if local_path.exists():
                                suffix = local_path.suffix.lower()
                                # Determine MIME type
                                if suffix in [".jpg", ".jpeg"]:
                                    mime_type = "image/jpeg"
                                elif suffix == ".png":
                                    mime_type = "image/png"
                                elif suffix == ".webp":
                                    mime_type = "image/webp"
                                elif suffix == ".gif":
                                    mime_type = "image/gif"
                                else:
                                    mime_type = "image/jpeg"  # Default

                                # Read and encode image
                                img_bytes = local_path.read_bytes()
                                base64_image = base64.b64encode(img_bytes).decode("utf-8")

                                # OpenAI format: data URL
                                new_content.append({
                                    "type": "image_url",
                                    "image_url": {
                                        "url": f"data:{mime_type};base64,{base64_image}"
                                    }
                                })
                            else:
                                # If file doesn't exist, keep original URL
                                new_content.append(part)
                        except Exception as e:
                            print(f"Could not process image for OpenAI: {e}")
                            new_content.append(part)
                    elif part.get("type") == "video_url":
                        # Handle videos - OpenAI doesn't support video directly, provide a note
                        url = part["video_url"]["url"]
                        new_content.append({
                            "type": "text",
                            "text": f"[Note: Video file attached: {url}. OpenAI GPT models don't support direct video analysis. Please use Google Gemini for video analysis.]"
                        })
                    elif part.get("type") == "document_url":
                        # For OpenAI, extract text from documents and append as text
                        url = part["document_url"]["url"]
                        try:
                            relative_path = url.split("/ui/", 1)[-1]
                            local_path = Path("Portal") / relative_path
                            if local_path.exists():
                                suffix = local_path.suffix.lower()
                                if suffix == ".txt":
                                    text_content = local_path.read_text(encoding='utf-8')
                                    new_content.append({
                                        "type": "text",
                                        "text": f"[Document content: {local_path.name}]\n\n{text_content}"
                                    })
                                else:
                                    # For other formats, add a note
                                    new_content.append({
                                        "type": "text",
                                        "text": f"[Note: Document {local_path.name} attached but content extraction not supported for this file type in OpenAI]"
                                    })
                        except Exception as e:
                            print(f"Could not process document for OpenAI: {e}")
                    else:
                        new_content.append(part)
                processed_messages.append({"role": m["role"], "content": new_content})
            else:
                processed_messages.append(m)
        else:
            processed_messages.append(m)

    headers = {"Authorization": f"Bearer {OPENAI_API_KEY}"}
    payload = {
        "model": model,
        "messages": processed_messages,
        "tools": _get_tools("openai", _last_user_msg(messages))
    }

    # Tool calling loop
    total_usage = {"prompt_tokens": 0, "completion_tokens": 0, "total_tokens": 0}
    max_tool_calls = 30

    for iteration in range(max_tool_calls):
        r = requests.post(OPENAI_URL, headers=headers, json=payload, timeout=200)
        if r.status_code != 200: raise HTTPException(r.status_code, r.text)

        data = r.json()
        message = data["choices"][0]["message"]
        finish_reason = data["choices"][0].get("finish_reason", "")

        # Accumulate usage
        u = data.get("usage", {})
        total_usage["prompt_tokens"] += u.get("prompt_tokens", 0)
        total_usage["completion_tokens"] += u.get("completion_tokens", 0)
        total_usage["total_tokens"] = total_usage["prompt_tokens"] + total_usage["completion_tokens"]

        # Check if model wants to use tools
        if finish_reason == "tool_calls" and message.get("tool_calls"):
            # Add assistant message to conversation
            payload["messages"].append(message)

            # Execute each tool call
            for tool_call in message["tool_calls"]:
                func = tool_call["function"]
                tool_name = func["name"]
                tool_id = tool_call["id"]
                args = json.loads(func["arguments"])

                if tool_name == "web_search":
                    query = args.get("query", "")
                    recency = args.get("search_recency_filter", "month")
                    print(f"[OPENAI] Executing web search: {query}")
                    result = perform_web_search(query, search_recency_filter=recency)
                elif tool_name == "web_fetch":
                    url = args.get("url", "")
                    max_chars = args.get("max_chars", 80000)
                    print(f"[OPENAI] Executing web fetch: {url}")
                    result = perform_web_fetch(url, max_chars)
                elif tool_name in ("list_devices", "control_android_device",
                                   "gmail_search", "gmail_read", "gmail_send", "gmail_reply", "gmail_labels"):
                    from Orchestrator.tools import BlackBoxToolExecutor
                    executor = BlackBoxToolExecutor(operator=operator)
                    import asyncio
                    tool_result = asyncio.run(executor.execute(tool_name, args))
                    result = tool_result.result
                    print(f"[OPENAI] {tool_name}: {result[:100]}")
                else:
                    result = f"Unknown tool: {tool_name}"

                # Add tool result to messages
                payload["messages"].append({
                    "role": "tool",
                    "tool_call_id": tool_id,
                    "content": result
                })

            print(f"[OPENAI] Tool iteration {iteration + 1}, continuing...")
        else:
            # No more tool calls, extract final text
            break

    text = message.get("content", "")
    return text, total_usage

def call_anthropic(messages: List[Dict], model: str, operator: str = "Brandon"):
    if not ANTHROPIC_API_KEY: raise HTTPException(400, "ANTHROPIC_API_KEY not set")
    system_text = "\n\n".join([m.get("content","") for m in messages if m.get("role") == "system"])
    amsgs = []
    for m in messages:
        if m.get("role") in ("user", "assistant"):
            content = m.get("content")
            if isinstance(content, list): # Handle multimodal content
                new_content = []
                for part in content:
                    # Handle direct base64 images (from Android app)
                    if part.get("type") == "image" and part.get("source", {}).get("type") == "base64":
                        # Already in Anthropic format - pass through directly
                        source = part.get("source", {})
                        media_type = source.get("media_type", "image/png")
                        data = source.get("data", "")
                        if data:
                            print(f"[ANTHROPIC] Processing direct base64 image ({len(data)} chars, {media_type})")
                            new_content.append({
                                "type": "image",
                                "source": { "type": "base64", "media_type": media_type, "data": data }
                            })
                    elif part.get("type") == "image_url":
                        url = part["image_url"]["url"]
                        try:
                            # Corrected path logic
                            relative_path = url.split("/ui/", 1)[-1]
                            local_path = Path("Portal") / relative_path
                            if local_path.exists():
                                media_type = "image/jpeg" if local_path.suffix.lower() in [".jpg", ".jpeg"] else "image/png"
                                img_bytes = local_path.read_bytes()
                                base64_image = base64.b64encode(img_bytes).decode("utf-8")
                                new_content.append({
                                    "type": "image",
                                    "source": { "type": "base64", "media_type": media_type, "data": base64_image }
                                })
                        except Exception as e:
                            print(f"Could not process local image for Anthropic: {e}")
                    elif part.get("type") == "video_url":
                        # Handle videos - Anthropic doesn't support video directly, provide a note
                        url = part["video_url"]["url"]
                        new_content.append({
                            "type": "text",
                            "text": f"[Note: Video file attached: {url}. Anthropic Claude models don't support direct video analysis. Please use Google Gemini for video analysis.]"
                        })
                    elif part.get("type") == "document_url":
                        # Anthropic supports PDF documents
                        url = part["document_url"]["url"]
                        try:
                            relative_path = url.split("/ui/", 1)[-1]
                            local_path = Path("Portal") / relative_path
                            if local_path.exists():
                                suffix = local_path.suffix.lower()
                                if suffix == ".pdf":
                                    doc_bytes = local_path.read_bytes()
                                    base64_doc = base64.b64encode(doc_bytes).decode("utf-8")
                                    new_content.append({
                                        "type": "document",
                                        "source": { "type": "base64", "media_type": "application/pdf", "data": base64_doc }
                                    })
                                elif suffix == ".txt":
                                    # For text files, just read and add as text
                                    text_content = local_path.read_text(encoding='utf-8')
                                    new_content.append({
                                        "type": "text",
                                        "text": f"[Document content: {local_path.name}]\n\n{text_content}"
                                    })
                        except Exception as e:
                            print(f"Could not process document for Anthropic: {e}")
                    elif part.get("type") == "text":
                        new_content.append(part)
                amsgs.append({"role": m["role"], "content": new_content})
            else: # Original text-only logic
                amsgs.append({"role": m["role"], "content": content})

    headers = {"x-api-key": ANTHROPIC_API_KEY, "anthropic-version": "2023-06-01", "content-type": "application/json"}

    # Include web search, image generation, and video generation tools for Claude
    payload = {
        "model": model,
        "max_tokens": 16000,
        "messages": amsgs,
        "tools": _get_tools("anthropic", _last_user_msg(messages))
    }
    # temperature removed on Opus 4.7 (returns 400). Keep on older models.
    if model not in ANTHROPIC_NO_SAMPLING_MODELS:
        payload["temperature"] = 0.2
    # Adaptive thinking + effort for Claude 4.X models that support it.
    # display="summarized" is gated to Opus 4.7 — older thinking models stream text as-is.
    if model in ANTHROPIC_THINKING_MODELS:
        thinking_cfg = {"type": "adaptive"}
        if model in ANTHROPIC_THINKING_DISPLAY_MODELS:
            thinking_cfg["display"] = "summarized"
        payload["thinking"] = thinking_cfg
        effort = ANTHROPIC_EFFORT_MAP.get(model)
        if effort:
            payload["output_config"] = {"effort": effort}
    if system_text:
        payload["system"] = system_text

    # Tool calling loop - continue until model stops using tools
    total_usage = {"prompt_tokens": 0, "completion_tokens": 0, "total_tokens": 0}
    max_tool_calls = 30  # Prevent infinite loops

    for iteration in range(max_tool_calls):
        r = requests.post(ANTHROPIC_URL, headers=headers, json=payload, timeout=200)
        if r.status_code != 200:
            raise HTTPException(r.status_code, r.text)

        data = r.json()
        blocks = data.get("content", [])
        stop_reason = data.get("stop_reason", "")

        # Accumulate usage
        u = data.get("usage", {})
        total_usage["prompt_tokens"] += u.get("input_tokens", 0)
        total_usage["completion_tokens"] += u.get("output_tokens", 0)
        total_usage["total_tokens"] = total_usage["prompt_tokens"] + total_usage["completion_tokens"]

        # Check if model wants to use tools
        if stop_reason == "tool_use":
            # Find tool use blocks
            tool_uses = [b for b in blocks if b.get("type") == "tool_use"]
            if not tool_uses:
                break  # No tool uses found, exit loop

            # Add assistant's response (with tool use) to messages
            payload["messages"].append({"role": "assistant", "content": blocks})

            # Process each tool use and add results
            tool_results = []
            for tool_use in tool_uses:
                tool_name = tool_use.get("name", "")
                tool_id = tool_use.get("id", "")
                tool_input = tool_use.get("input", {})

                if tool_name == "web_search":
                    query = tool_input.get("query", "")
                    recency = tool_input.get("search_recency_filter", "month")
                    print(f"[ANTHROPIC] Executing web search: {query}")
                    search_result = perform_web_search(query, search_recency_filter=recency)
                    tool_results.append({
                        "type": "tool_result",
                        "tool_use_id": tool_id,
                        "content": search_result
                    })
                elif tool_name == "web_fetch":
                    url = tool_input.get("url", "")
                    max_chars = tool_input.get("max_chars", 80000)
                    print(f"[ANTHROPIC] Executing web fetch: {url}")
                    fetch_result = perform_web_fetch(url, max_chars)
                    tool_results.append({
                        "type": "tool_result",
                        "tool_use_id": tool_id,
                        "content": fetch_result
                    })
                elif tool_name == "generate_image":
                    prompt = tool_input.get("prompt", "")
                    aspect_ratio = tool_input.get("aspectRatio", "16:9")
                    resolution = tool_input.get("resolution", "1K")
                    num_images = tool_input.get("numberOfImages", 1)
                    reference_images = tool_input.get("reference_images", [])
                    mode = "image-to-image" if reference_images else "text-to-image"
                    print(f"[ANTHROPIC] Executing {mode} generation: {prompt[:100]}... ({num_images} @ {resolution}, {len(reference_images)} refs)")

                    # Build image generation options
                    image_options = {
                        "aspectRatio": aspect_ratio,
                        "resolution": resolution,
                        "numberOfImages": num_images,
                    }

                    # Process reference images for image-to-image generation
                    reference_image_data = []
                    if reference_images:
                        import base64 as b64
                        from pathlib import Path as P
                        for img_url in reference_images[:14]:  # Max 14 images
                            clean_url = img_url.replace("/ui/", "") if img_url.startswith("/ui/") else img_url
                            file_path = P("Portal") / clean_url
                            if file_path.exists():
                                with open(file_path, "rb") as f:
                                    img_b64 = b64.b64encode(f.read()).decode()
                                ext = file_path.suffix.lower()
                                mime_map = {".png": "image/png", ".jpg": "image/jpeg", ".jpeg": "image/jpeg", ".webp": "image/webp", ".gif": "image/gif"}
                                reference_image_data.append({
                                    "data": img_b64,
                                    "mimeType": mime_map.get(ext, "image/jpeg")
                                })
                        if reference_image_data:
                            image_options["referenceImages"] = reference_image_data

                    task = create_task(
                        TaskType.IMAGE_GENERATION,
                        operator="system",  # Placeholder, actual operator set by UI
                        prompt=prompt,
                        result_data={"options": image_options}
                    )

                    # Generate predicted URL (same logic as process_image_generation)
                    slug = generate_prompt_slug(prompt)
                    if num_images == 1:
                        predicted_url = f"/ui/uploads/{slug}_{task.task_id}.png"
                        url_info = f"Image will be saved to: {predicted_url}"
                    else:
                        predicted_urls = [f"/ui/uploads/{slug}_{task.task_id}_{i+1}.png" for i in range(num_images)]
                        url_info = f"Images will be saved to: {', '.join(predicted_urls)}"

                    img_count = num_images
                    result_message = f"Image generation started for: {prompt[:100]}{'...' if len(prompt) > 100 else ''}. Creating {img_count} image{'s' if img_count > 1 else ''} at {resolution} resolution ({aspect_ratio} aspect ratio). {url_info}. To use this image for video generation later, pass the URL to generate_video with image_url parameter."

                    tool_results.append({
                        "type": "tool_result",
                        "tool_use_id": tool_id,
                        "content": result_message
                    })
                elif tool_name == "generate_video":
                    prompt = tool_input.get("prompt", "")
                    video_url = tool_input.get("video_url")
                    image_url = tool_input.get("image_url")
                    aspect_ratio = tool_input.get("aspectRatio", "16:9")
                    duration = tool_input.get("duration", 8)
                    resolution = tool_input.get("resolution", "720p")
                    negative_prompt = tool_input.get("negativePrompt", "")

                    # Determine mode: video extension takes priority
                    if video_url:
                        mode = "video-extension"
                    elif image_url:
                        mode = "image-to-video"
                    else:
                        mode = "text-to-video"
                    print(f"[ANTHROPIC] Executing video generation ({mode}): {prompt[:100]}... ({duration}s @ {resolution})")

                    # Generate predicted URL
                    slug = generate_prompt_slug(prompt) if prompt else "video"

                    if video_url:
                        # VIDEO EXTENSION MODE - create task with url for extension
                        video_data = {
                            "url": video_url,
                            "prompt": prompt
                        }
                        task = create_task(
                            TaskType.VIDEO_GENERATION,
                            operator="system",
                            prompt=prompt,
                            result_data=video_data
                        )
                        predicted_url = f"/ui/uploads/{slug}_{task.task_id}.mp4"
                        result_message = f"Video EXTENSION started. Extending video at {video_url} with prompt: {prompt[:100]}{'...' if len(prompt) > 100 else ''}. The new clip will continue from where the original ended. Video will be saved to: {predicted_url}. Takes 5-20 minutes."
                    else:
                        # TEXT-TO-VIDEO or IMAGE-TO-VIDEO
                        video_options = {
                            "aspectRatio": aspect_ratio,
                            "duration": duration,
                            "resolution": resolution,
                        }
                        if negative_prompt:
                            video_options["negativePrompt"] = negative_prompt
                        if image_url:
                            video_options["image_url"] = image_url

                        # If image_url provided, convert to base64 for the task
                        image_data = None
                        if image_url:
                            import base64 as b64
