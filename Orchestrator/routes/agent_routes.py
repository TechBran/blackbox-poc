# Track network I/O for rate calculation
_last_net_io = None
_last_net_time = None

@app.get("/system/metrics")
async def get_system_metrics():
    """Get real-time system metrics for BlackBox system monitor."""
    global _last_net_io, _last_net_time

    try:
        # CPU metrics
        cpu_percent = psutil.cpu_percent(interval=0.1)
        cpu_count = psutil.cpu_count()
        load_avg = psutil.getloadavg() if hasattr(psutil, 'getloadavg') else [0, 0, 0]

        # CPU Temperature (Linux only, may not work on all systems)
        cpu_temp = 0
        try:
            if hasattr(psutil, 'sensors_temperatures'):
                temps = psutil.sensors_temperatures()
                if temps:
                    # Try common sensor names
                    for name in ['coretemp', 'k10temp', 'cpu_thermal', 'cpu-thermal']:
                        if name in temps:
                            cpu_temp = temps[name][0].current
                            break
                    # If no match, use first available
                    if cpu_temp == 0 and temps:
                        first_sensor = list(temps.values())[0]
                        if first_sensor:
                            cpu_temp = first_sensor[0].current
        except Exception as e:
            print(f"[METRICS] Temperature read error: {e}")

        # Memory metrics
        memory = psutil.virtual_memory()

        # Disk metrics (root partition)
        disk = psutil.disk_usage('/')

        # Network metrics with rate calculation
        net_io = psutil.net_io_counters()
        current_time = time.time()

        upload_rate = 0
        download_rate = 0

        if _last_net_io and _last_net_time:
            time_delta = current_time - _last_net_time
            if time_delta > 0:
                upload_rate = (net_io.bytes_sent - _last_net_io.bytes_sent) / time_delta
                download_rate = (net_io.bytes_recv - _last_net_io.bytes_recv) / time_delta

        _last_net_io = net_io
        _last_net_time = current_time

        # System info
        hostname = socket.gethostname()
        platform_info = f"{platform.system()} {platform.release()}"
        uptime = time.time() - psutil.boot_time()

        # Top processes by CPU
        processes = []
        for proc in psutil.process_iter(['pid', 'name', 'cpu_percent', 'memory_percent']):
            try:
                pinfo = proc.info
                processes.append({
                    'pid': pinfo['pid'],
                    'name': pinfo['name'],
                    'cpu_percent': pinfo['cpu_percent'] or 0,
                    'memory_percent': pinfo['memory_percent'] or 0
                })
            except (psutil.NoSuchProcess, psutil.AccessDenied):
                pass

        # Sort by CPU usage and take top 10
        processes.sort(key=lambda x: x['cpu_percent'], reverse=True)
        top_processes = processes[:10]

        return {
            'hostname': hostname,
            'platform': platform_info,
            'uptime': uptime,
            'cpu': {
                'percent': cpu_percent,
                'cores': cpu_count,
                'load_avg': list(load_avg),
                'temperature': cpu_temp
            },
            'memory': {
                'total': memory.total,
                'used': memory.used,
                'available': memory.available,
                'percent': memory.percent
            },
            'disk': {
                'total': disk.total,
                'used': disk.used,
                'free': disk.free,
                'percent': disk.percent
            },
            'network': {
                'upload_rate': upload_rate,  # bytes/sec
                'download_rate': download_rate,  # bytes/sec
                'upload_total': net_io.bytes_sent,
                'download_total': net_io.bytes_recv
            },
            'processes': top_processes
        }

    except Exception as e:
        print(f"[METRICS] Error collecting metrics: {e}")
        raise HTTPException(500, f"Failed to collect system metrics: {str(e)}")


@app.delete("/agent/session/{session_id}")
async def terminate_agent_session(session_id: str):
    """Terminate an agent session and cleanup."""
    with agent_lock:
        session = AGENT_SESSIONS.get(session_id)
        if session:
            if session.process and session.process.poll() is None:
                session.process.terminate()
                try:
                    session.process.wait(timeout=5)
                except:
                    session.process.kill()

            if session.pty_master:
                try:
                    os.close(session.pty_master)
                except:
                    pass

            del AGENT_SESSIONS[session_id]
            print(f"[AGENT] Terminated session {session_id}")
            return {"status": "terminated"}

    return {"status": "not_found"}


