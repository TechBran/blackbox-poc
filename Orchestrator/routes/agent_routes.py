async def _inject_fossil_context(
    websocket: WebSocket,
    operator_raw: str,
    user_text: str,
    log_prefix: str,
) -> tuple[str, dict]:
    """Run shared retrieval and emit provenance to the client.

    Returns (fossil_context_str, provenance_dict). On any failure (or empty
    operator) returns an empty fossil string + empty provenance so the caller
    can proceed without context. Always emits a `provenance` WS frame so the
    client UI has a deterministic signal — even if every list is empty.
    """
    operator = resolve_operator(operator_raw, log_prefix)
    fossil_text, provenance = retrieve_for_agent(
        user_text=user_text,
        operator=operator,
        log_prefix=log_prefix,
    )
    # Ordering invariant: provenance MUST emit BEFORE we spawn Claude. Android's
    # ClaudeAgentClient writes provenance onto the *current* streaming UiMessage;
    # if we emit after stream completion, the event lands on the next bubble.
    await _safe_ws_send(websocket, {"type": "provenance", "data": provenance})
    return fossil_text, provenance


async def _safe_ws_send(websocket, data: dict) -> bool:
    """Send JSON to WebSocket, return False if connection is dead."""
    try:
        if websocket.application_state == WebSocketState.CONNECTED:
            await websocket.send_json(data)
            return True
    except Exception:
        pass
    return False


def create_claude_streaming(working_dir: str, prompt: str, claude_session_id: str = None, continue_session: bool = False, resume_session: bool = False, model: str = None, skip_permissions: bool = True) -> subprocess.Popen:
    """Create Claude Code process in streaming print mode.
    Uses -p (print) mode with stream-json output for programmatic use.
    This completely skips trust dialogs and interactive prompts.
    Args:
        working_dir: Working directory for Claude Code
        prompt: The prompt to send
        claude_session_id: UUID for Claude's session tracking (for --session-id or --resume)
        continue_session: If True, use --continue to continue the conversation
        resume_session: If True, use --resume to restore a session from disk (requires claude_session_id)
        model: Model to use (opus, sonnet, haiku). If None, uses default.
        skip_permissions: If True (default), bypass permission checks. If False, require permission approval.
    Returns:
        subprocess.Popen with stdout pipe for reading JSON stream
    """
    claude_path = os.path.expanduser("~/.local/bin/claude")
    if not os.path.exists(claude_path):
        claude_path = "claude"

    # Build command arguments
    cmd = [
        claude_path,
        "-p",  # Print mode - skips trust dialog
        "--output-format", "stream-json",  # Streaming JSON output
        "--verbose",  # Required for stream-json
    ]

    # Conditionally skip permissions based on user preference
    if skip_permissions:
        cmd.append("--dangerously-skip-permissions")

    # Add model selection if specified
    if model:
        cmd.extend(["--model", model])

    # Add session continuity flags (priority: resume > continue > session-id)
    if resume_session and claude_session_id:
        cmd.extend(["--resume", claude_session_id])  # Resume a specific session from disk
        print(f"[AGENT] Resuming session from disk: {claude_session_id[:8]}...")
    elif continue_session:
        cmd.append("--continue")  # Continue the most recent conversation in this directory
    elif claude_session_id:
        cmd.extend(["--session-id", claude_session_id])

    # Add the prompt
    cmd.append(prompt)

    print(f"[AGENT] Claude command: {' '.join(cmd[:8])}... (continue={continue_session}, resume={resume_session}, session_id={claude_session_id[:8] if claude_session_id else 'None'})")
    print(f"[AGENT] Model specified: {model}")

    # Only use stdin pipe when we need to send permission responses
    # Having stdin=PIPE when not needed can cause Claude Code to hang
    stdin_config = subprocess.PIPE if not skip_permissions else subprocess.DEVNULL

    process = subprocess.Popen(
        cmd,
        stdin=stdin_config,
        stdout=subprocess.PIPE,
        stderr=subprocess.PIPE,
        cwd=working_dir,
        env={
            **os.environ,
            "PATH": os.environ.get("PATH", "") + ":" + os.path.expanduser("~/.local/bin")
        },
        start_new_session=True
    )

    # Set stdout to non-blocking
    flags = fcntl.fcntl(process.stdout.fileno(), fcntl.F_GETFL)
    fcntl.fcntl(process.stdout.fileno(), fcntl.F_SETFL, flags | os.O_NONBLOCK)

    print(f"[AGENT] Created Claude Code streaming process PID={process.pid} in {working_dir}")
    return process


def create_claude_pty(working_dir: str) -> Tuple[Any, int]:
    """Create PTY-controlled Claude Code process (legacy - for interactive mode).
    Returns:
        Tuple of (subprocess.Popen, master_fd)
    """
