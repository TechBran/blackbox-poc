@app.api_route("/app-proxy/{port}/{path:path}", methods=["GET", "POST", "PUT", "DELETE", "PATCH", "OPTIONS"])
async def app_proxy(port: int, path: str, request: Request):
    """Reverse proxy to locally running apps created by Claude Code.
    This allows viewing apps through the Portal iframe without exposing ports.
    """
    # Validate port is reasonable
    if port < 1024 or port > 65535:
        raise HTTPException(400, "Invalid port number")

    target_url = f"http://localhost:{port}/{path}"

    # Handle query string
    if request.url.query:
        target_url += f"?{request.url.query}"

    try:
        async with httpx.AsyncClient(timeout=30.0, follow_redirects=True) as client:
            # Build headers (exclude hop-by-hop headers)
            headers = {}
            for key, value in request.headers.items():
                if key.lower() not in ('host', 'content-length', 'transfer-encoding', 'connection'):
                    headers[key] = value

            # Forward request
            if request.method == "GET":
                resp = await client.get(target_url, headers=headers)
            elif request.method == "POST":
                body = await request.body()
                resp = await client.post(target_url, content=body, headers=headers)
            elif request.method == "PUT":
                body = await request.body()
                resp = await client.put(target_url, content=body, headers=headers)
            elif request.method == "DELETE":
                resp = await client.delete(target_url, headers=headers)
            elif request.method == "PATCH":
                body = await request.body()
                resp = await client.patch(target_url, content=body, headers=headers)
            elif request.method == "OPTIONS":
                resp = await client.options(target_url, headers=headers)
            else:
                resp = await client.request(request.method, target_url, headers=headers)

            content = resp.content
            content_type = resp.headers.get("content-type", "application/octet-stream")

            # Rewrite relative URLs in HTML responses
            if "text/html" in content_type:
                try:
                    html = content.decode('utf-8', errors='replace')
                    # Rewrite href, src, action attributes that start with /
                    html = re.sub(r'(href|src|action)=["\']/', f'\\1="/app-proxy/{port}/', html)
                    content = html.encode('utf-8')
                except Exception as e:
                    print(f"[APP-PROXY] HTML rewrite error: {e}")

            # Build response headers
            response_headers = {}
            for key, value in resp.headers.items():
                if key.lower() not in ('transfer-encoding', 'content-encoding', 'content-length'):
                    response_headers[key] = value

            # Force no-cache to ensure fresh content through proxy
            response_headers['Cache-Control'] = 'no-store, no-cache, must-revalidate, max-age=0'
            response_headers['Pragma'] = 'no-cache'
            response_headers['Expires'] = '0'

            return Response(
                content=content,
                status_code=resp.status_code,
                headers=response_headers,
                media_type=content_type
            )

    except httpx.ConnectError:
        raise HTTPException(503, f"Cannot connect to app on port {port}. Is it running?")
    except httpx.TimeoutException:
        raise HTTPException(504, f"Request to app on port {port} timed out")
    except Exception as e:
        print(f"[APP-PROXY] Error: {e}")
        raise HTTPException(500, f"Proxy error: {str(e)}")


