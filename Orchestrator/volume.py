def parse_tail(vol_txt: str) -> Dict[str, Any]:
    ends = list(END_RX.finditer(vol_txt))
    if not ends:
        return {"unanimous": True, "tail_id": "SNAP-000", "utc": "N/A"}
    end_m = ends[-1]
    snap_id = end_m.group("snap")
    utc_part = end_m.group("utc")
    end_pos = end_m.start()
    starts_before = list(START_RX.finditer(vol_txt, 0, end_pos))
    start_m = next((m for m in reversed(starts_before) if m.group("snap") == snap_id), None)
    if not start_m:
        raise HTTPException(500, "PARTIAL-SNAPSHOT")
    return {"unanimous": True, "tail_id": snap_id, "utc": utc_part}

