def cleanup_old_archives(days_to_keep: int = 30, max_size_gb: float = 5.0, max_files: int = 200) -> Dict[str, Any]:
    """Clean up old archive files to prevent disk space issues.

    Args:
        days_to_keep: Keep archives newer than this many days
        max_size_gb: Maximum total size of archive directory in GB
        max_files: Maximum number of archive files to keep

    Returns:
        Dict with cleanup statistics
    """
    import shutil
    from datetime import datetime, timedelta, timezone

    cleanup_stats = {
        "deleted_files": 0,
        "deleted_size_mb": 0,
        "remaining_files": 0,
        "remaining_size_mb": 0,
        "reason": []
    }

    if not ARC_DIR.exists():
        return cleanup_stats

    # Get all archive files sorted by modification time (oldest first)
    archive_files = []
    total_size = 0

    for file_path in ARC_DIR.glob("Volume_*.txt"):
        stat = file_path.stat()
        archive_files.append({
            "path": file_path,
            "mtime": stat.st_mtime,
            "size": stat.st_size
        })
        total_size += stat.st_size

    if not archive_files:
        return cleanup_stats

    # Sort by modification time (oldest first)
    archive_files.sort(key=lambda x: x["mtime"])

    # Determine what to delete
    files_to_delete = []
    cutoff_time = time.time() - (days_to_keep * 86400)  # Convert days to seconds

    # Check age-based cleanup
    for file_info in archive_files:
        if file_info["mtime"] < cutoff_time:
            files_to_delete.append(file_info)
            cleanup_stats["reason"].append("age")

    # Check size-based cleanup (delete oldest until under size limit)
    total_size_gb = total_size / (1024 ** 3)
    if total_size_gb > max_size_gb:
        cleanup_stats["reason"].append("size")
        for file_info in archive_files:
            if total_size_gb <= max_size_gb:
                break
            if file_info not in files_to_delete:
                files_to_delete.append(file_info)
            total_size_gb -= (file_info["size"] / (1024 ** 3))

    # Check count-based cleanup (keep only the most recent max_files)
    if len(archive_files) > max_files:
        cleanup_stats["reason"].append("count")
        files_to_keep = archive_files[-max_files:]  # Keep the most recent
        for file_info in archive_files:
            if file_info not in files_to_keep and file_info not in files_to_delete:
                files_to_delete.append(file_info)

    # Perform deletion
    for file_info in files_to_delete:
        try:
            file_info["path"].unlink()
            cleanup_stats["deleted_files"] += 1
            cleanup_stats["deleted_size_mb"] += file_info["size"] / (1024 ** 2)
        except Exception as e:
            print(f"Error deleting archive {file_info['path']}: {e}")

    # Calculate remaining statistics
    remaining_size = 0
    remaining_count = 0
    for file_info in archive_files:
        if file_info not in files_to_delete:
            remaining_count += 1
            remaining_size += file_info["size"]

    cleanup_stats["remaining_files"] = remaining_count
    cleanup_stats["remaining_size_mb"] = remaining_size / (1024 ** 2)

    return cleanup_stats

