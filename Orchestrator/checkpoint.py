def create_checkpoint_async(operator: str, manual: bool = False):
    """Create a compressed checkpoint snapshot for an operator.

    This function:
    1. Retrieves recent snapshots for the operator (count configured in [checkpoint] section)
    2. Sends them to Gemini 3.1 Pro for compression
    3. Saves the compressed summary as a checkpoint snapshot

    Args:
        operator: The operator name (e.g., "Brandon", "Anna")
        manual: Whether this is a manual checkpoint (vs automatic at configured intervals)
    """
    try:
        s = get_state(operator)
        vol_txt = read_text_safe(VOL_PATH)

        # Get recent snapshots for this operator (configured in [checkpoint] section)
        recent_snapshots = get_recent_fossils_for_operator(vol_txt, operator, count=CHECKPOINT_TURNS_TO_COMPRESS, cap_chars_each=10000)

        if len(recent_snapshots) < CHECKPOINT_MIN_SNAPSHOTS:
            print(f"[CHECKPOINT] Not enough snapshots for {operator} to create checkpoint ({len(recent_snapshots)}/{CHECKPOINT_TURNS_TO_COMPRESS})")
            return

        print(f"[CHECKPOINT] Creating checkpoint for {operator} with {len(recent_snapshots)} snapshots")

        # Prepare compression prompt
        snapshots_text = "\n\n" + "="*80 + "\n\n".join(recent_snapshots)

        compression_prompt = f"""Compress the following {len(recent_snapshots)} conversation turns into a detailed, coherent summary that preserves key context, decisions, technical details, and conversation flow.

Maintain enough detail that someone reading only this summary could continue the conversation meaningfully.

Focus on:
- Main topics discussed
- Decisions made
- Technical implementations
- User preferences expressed
- Important context that should be remembered
- Key problems solved
- System architecture details
- Feature requests and requirements

Provide a comprehensive, structured summary:

{snapshots_text}"""

        # Call Gemini 3.1 Pro for compression (largest context window)
        messages = [{"role": "user", "content": compression_prompt}]

        # Use Gemini 3.1 Pro for maximum context window (lazy import to avoid circular import)
        from Orchestrator.routes.chat_routes import call_gemini
        compressed_summary, usage, _ = call_gemini(messages, model="gemini-3.1-pro-preview", operator=operator)

        if not compressed_summary:
            print(f"[CHECKPOINT] Failed to generate compression for {operator}")
            return

        # Create checkpoint snapshot
        with mint_lock:
            utc = now_utc_iso()
            vol1 = read_text_safe(VOL_PATH)
            info1 = parse_tail(vol1)
            snap_id = next_snap_id_from_tail(info1["tail_id"])

            # Determine source range
            source_start = s.total_turns - len(recent_snapshots)
            source_end = s.total_turns

            reason = "MANUAL_CHECKPOINT" if manual else f"AUTO_CHECKPOINT_TURN_{s.total_turns}"

            # Build checkpoint snapshot body
            log_lines = [
                f"[CHECKPOINT] Compressed summary of {len(recent_snapshots)} snapshots",
                f"[CHECKPOINT] Source range: turns {source_start}-{source_end}",
                f"[CHECKPOINT] Operator: {operator}",
                "",
                compressed_summary
            ]

            gauges = {
                "drift": "green",
                "p": 0,
                "c": len(compressed_summary),
                "t": len(compressed_summary),
                "model": "gemini-3.1-pro-preview",
                "operator": operator
            }

            provenance = {
                "gm": True,
                "recent": [],
                "relevant": []
            }

            # Render checkpoint snapshot with special metadata
            body = render_snapshot_body_v71(
                info=info1,
                snap_id=snap_id,
                utc=utc,
                reason=reason,
                log_lines=log_lines,
                gauges=gauges,
                provenance=provenance
            )

            # Add checkpoint-specific metadata
            body = body.replace("MODE: Normal", "MODE: Checkpoint")
            body = body.replace(f"OPERATOR: {operator}", f"OPERATOR: {operator}\nTYPE: checkpoint\nSOURCE_RANGE: {source_start}-{source_end}")

            # Append checkpoint to volume
            byte_start = VOL_PATH.stat().st_size if VOL_PATH.exists() else 0
            append_snapshot_text(body)
            byte_end = VOL_PATH.stat().st_size

            # Generate embedding for semantic search
            print(f"[EMBEDDING] Generating embedding for checkpoint {snap_id}...")
            embedding = generate_embedding(body)
            if embedding:
                print(f"[EMBEDDING] Successfully generated embedding ({len(embedding)} dimensions)")
            else:
                print(f"[EMBEDDING] Warning: Failed to generate embedding for {snap_id}")

            # Update index with embedding
            update_snapshot_index(
                snap_id=snap_id,
                byte_start=byte_start,
                byte_end=byte_end,
                operator=operator,
                timestamp=utc,
                snap_type="checkpoint",
                embedding=embedding
            )

            # Update operator state
            s.last_checkpoint_turn = s.total_turns
            s.last_checkpoint_id = snap_id

            print(f"[CHECKPOINT] Created {snap_id} for {operator} at turn {s.total_turns} ({len(compressed_summary)} chars)")

            # Save operator state after checkpoint creation
            save_operator_state()

    except Exception as e:
        print(f"[CHECKPOINT] Error creating checkpoint for {operator}: {e}")
        import traceback
        traceback.print_exc()


def should_create_checkpoint(operator: str) -> bool:
    """Check if a checkpoint should be created for this operator.

    Creates checkpoints when turns_since_last_checkpoint >= auto_create_interval.
    This matches the UI countdown timer (when it hits 0).
    """
    s = get_state(operator)

    # Calculate turns since last checkpoint (this is what the UI countdown shows)
    turns_since_last_checkpoint = s.total_turns - s.last_checkpoint_turn
    turns_remaining = CHECKPOINT_AUTO_CREATE_INTERVAL - turns_since_last_checkpoint

    # Debug logging
    print(f"[CHECKPOINT-CHECK] Operator: {operator}, total_turns: {s.total_turns}, last_checkpoint_turn: {s.last_checkpoint_turn}")
    print(f"[CHECKPOINT-CHECK] Turns since last checkpoint: {turns_since_last_checkpoint}, Turns remaining: {turns_remaining}, Interval: {CHECKPOINT_AUTO_CREATE_INTERVAL}")

    # Create checkpoint when we've reached or exceeded the interval (countdown hits 0 or below)
    if turns_since_last_checkpoint >= CHECKPOINT_AUTO_CREATE_INTERVAL:
        print(f"[CHECKPOINT-CHECK] Checkpoint needed! (counter hit 0)")
        return True
    else:
        print(f"[CHECKPOINT-CHECK] Not yet - {turns_remaining} turns remaining until checkpoint")
        return False

