def perform_mint(operator: str, reason: str = "MANUAL") -> Dict[str, Any]:
    verify_gm_or_halt()
    s = get_state(operator)

    with mint_lock:
        vol1 = read_text_safe(VOL_PATH)
        info1 = parse_tail(vol1)
        utc = now_utc_iso()
        snap_id = next_snap_id_from_tail(info1["tail_id"])

        new_items = s.conversation_log[s.last_mint_index:]

        def _render_log_line(it: dict) -> str:
            if it.get("role") == "user":
                t = it.get("text", "")
            else:
                t = it.get("snap_text") or it.get("text", "")
            return f"{it['utc']} operator={operator} {it['role']}: {t}"

        log_lines = [_render_log_line(it) for it in new_items]

        usage_last = s.last_context_meta.get("usage_last", {})
        p = usage_last.get("prompt_tokens", 0)
        c = usage_last.get("completion_tokens", 0)
        t = usage_last.get("total_tokens", p + c)
        model_name = s.last_context_meta.get("model", "")
        drift = drift_state_for(s)

        gm_flag = s.last_context_meta.get("gm_excerpt", True)
        recent_ids = s.last_context_meta.get("recent_ids", [])
        relevant_ids = s.last_context_meta.get("relevant_ids", [])

        # DEBUG: Log what provenance is being saved to snapshot
        print(f"[MINT] Creating snapshot {snap_id} for operator {operator}")
        print(f"[MINT] Provenance - Recent: {recent_ids}")
        print(f"[MINT] Provenance - Relevant: {relevant_ids}")

        body = render_snapshot_body_v71(
            info1, snap_id, utc, reason, log_lines,
            gauges={"drift": drift, "p": p, "c": c, "t": t, "model": model_name, "operator": operator},
            provenance={"gm": gm_flag, "recent": recent_ids, "relevant": relevant_ids},
        )

        vol2 = read_text_safe(VOL_PATH)
        info2 = parse_tail(vol2)
        if info2["tail_id"] != info1["tail_id"]:
            info1 = info2
            snap_id = next_snap_id_from_tail(info1["tail_id"])
            body = render_snapshot_body_v71(
                info1, snap_id, utc, reason, log_lines,
                gauges={"drift": drift, "p": p, "c": c, "t": t, "model": model_name, "operator": operator},
                provenance={"gm": gm_flag, "recent": recent_ids, "relevant": relevant_ids},
            )

        # Get file size before append to calculate byte offsets
        byte_start = VOL_PATH.stat().st_size if VOL_PATH.exists() else 0

        append_snapshot_text(body)

        # Get file size after append
        byte_end = VOL_PATH.stat().st_size if VOL_PATH.exists() else 0

        # Generate embedding for semantic search
        print(f"[EMBEDDING] Generating embedding for snapshot {snap_id}...")
        embedding = generate_embedding(body)
        if embedding:
            print(f"[EMBEDDING] Successfully generated embedding ({len(embedding)} dimensions)")
        else:
            print(f"[EMBEDDING] Warning: Failed to generate embedding for {snap_id}")

        # Collect media artifacts from recent tasks for this operator
        # Lazy import to avoid circular dependency
        from Orchestrator.tasks import collect_pending_media_artifacts
        media_artifacts = collect_pending_media_artifacts(operator)
        if media_artifacts:
            print(f"[MINT] Collected {len(media_artifacts)} media artifact(s) for {snap_id}")

        # Update the snapshot index with embedding and media artifacts
        update_snapshot_index(snap_id, byte_start, byte_end, operator, utc, snap_type="normal", embedding=embedding, media_artifacts=media_artifacts if media_artifacts else None)

        arc_path, digest, arc_utc = archive_volume()

        s.last_mint_index = len(s.conversation_log)
        s.conv_turns_since  = 0
        s.conv_tokens_since = 0
        s.ctx_tokens_since  = 0
        s.context_used      = 0

        # Save operator state after mint
        save_operator_state()

        return {"snap_id": snap_id, "assert_sha256": digest, "archived": Path(arc_path).name}


def mint_with_content(operator: str, content: str, reason: str = "DIRECT", snap_type: str = "normal") -> Dict[str, Any]:
    """Mint a snapshot with provided content (for MCP tools, CLI agents, etc.).

    Unlike perform_mint which pulls from conversation log, this creates a snapshot
    directly from the provided content string.

    Args:
        operator: The operator name (e.g., "Brandon-DEV")
        content: The content to save in the snapshot
        reason: Reason for minting (e.g., "DIRECT", "MCP", "AGENT")
        snap_type: Type of snapshot ("normal", "checkpoint", "summary")

    Returns:
        Dict with snap_id and archive info
    """
    verify_gm_or_halt()

    with mint_lock:
        vol1 = read_text_safe(VOL_PATH)
        info1 = parse_tail(vol1)
        utc = now_utc_iso()
        snap_id = next_snap_id_from_tail(info1["tail_id"])

        # Convert content to log lines format
        # Each line prefixed with timestamp and operator
        log_lines = [f"{utc} operator={operator} content: {content}"]

        print(f"[MINT-CONTENT] Creating snapshot {snap_id} for operator {operator}")
        print(f"[MINT-CONTENT] Content length: {len(content)} chars")

        body = render_snapshot_body_v71(
            info1, snap_id, utc, reason, log_lines,
            gauges={"drift": "green", "p": 0, "c": 0, "t": 0, "model": "direct-content", "operator": operator},
            provenance={"gm": False, "recent": [], "relevant": []},
        )

        # Handle race condition
        vol2 = read_text_safe(VOL_PATH)
        info2 = parse_tail(vol2)
        if info2["tail_id"] != info1["tail_id"]:
            info1 = info2
            snap_id = next_snap_id_from_tail(info1["tail_id"])
            body = render_snapshot_body_v71(
                info1, snap_id, utc, reason, log_lines,
                gauges={"drift": "green", "p": 0, "c": 0, "t": 0, "model": "direct-content", "operator": operator},
                provenance={"gm": False, "recent": [], "relevant": []},
            )

        # Get file size before append
        byte_start = VOL_PATH.stat().st_size if VOL_PATH.exists() else 0

        append_snapshot_text(body)

        # Get file size after append
        byte_end = VOL_PATH.stat().st_size if VOL_PATH.exists() else 0

        # Generate embedding for semantic search
        print(f"[EMBEDDING] Generating embedding for snapshot {snap_id}...")
        embedding = generate_embedding(body)
        if embedding:
            print(f"[EMBEDDING] Successfully generated embedding ({len(embedding)} dimensions)")
        else:
            print(f"[EMBEDDING] Warning: Failed to generate embedding for {snap_id}")

        # Update the snapshot index
        update_snapshot_index(snap_id, byte_start, byte_end, operator, utc, snap_type=snap_type, embedding=embedding)

        arc_path, digest, arc_utc = archive_volume()

        return {"snap_id": snap_id, "assert_sha256": digest, "archived": Path(arc_path).name, "byte_range": [byte_start, byte_end]}


